package control_statements.conditional;

public class Vowels_consonant {
	public static void main(String[] args) {

		char ch='w';

		if((ch =='a'))

		{
			System.out.println(ch+" is a Vowel");
		}
		else if(ch =='e')

		{
			System.out.println(ch+" is a Vowel");
		}
		else if(ch =='i')

		{
			System.out.println(ch+" is a Vowel");
		}
		else if(ch =='o')

		{
			System.out.println(ch+" is a Vowel");
		}
		else if(ch =='u')

		{
			System.out.println(ch+" is a Vowel");
		}
		else

		{
			System.out.println(ch+" is a consonant ");
		}
	}

}
